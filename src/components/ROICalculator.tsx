            <div className="p-3 bg-indigo-500/20 rounded-xl border border-indigo-500/30">
              <Calculator className="w-6 h-6 text-indigo-400" />
            </div>
            <div>
              <h2 className="text-xl font-bold text-white">ROI Calculator</h2>
